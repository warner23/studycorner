        <div class="title">Endocrine System</div>
        <p>
The endocrine system includes all of the glands of the body and the hormones produced by those glands. The glands are controlled directly by stimulation from the nervous system as well as by chemical receptors in the blood and hormones produced by other glands. By regulating the functions of organs in the body, these glands help to maintain the body’s homeostasis. Cellular metabolism, reproduction, sexual development, sugar and mineral homeostasis, heart rate, and digestion are among the many processes regulated by the actions of hormones.
        </p>
        </div>

        <div class="skele">
        <div class="title">Hypothalamus</div>
        <p>
         The hypothalamus is a part of the brain located superior and anterior to the brain stem and inferior to the thalamus. It serves many different functions in the nervous system, and is also responsible for the direct control of the endocrine system through the pituitary gland. The hypothalamus contains special cells called neurosecretory cells—neurons that secrete hormones:


          <ol>
          <li>Thyrotropin-releasing hormone (TRH)</li>
          <li>Growth hormone-releasing hormone (GHRH)</li>
          <li>Growth hormone-inhibiting hormone (GHIH)</li>
          <li>Gonadotropin-releasing hormone (GnRH)</li>
          <li>Corticotropin-releasing hormone (CRH)</li>
          <li>Oxytocin</li>
          <li>Antidiuretic hormone (ADH)</li>
          </ol>
          <br />

          All of the releasing and inhibiting hormones affect the function of the anterior pituitary gland. TRH stimulates the anterior pituitary gland to release thyroid-stimulating hormone. GHRH and GHIH work to regulate the release of growth hormone—GHRH stimulates growth hormone release, GHIH inhibits its release. GnRH stimulates the release of follicle stimulating hormone and luteinizing hormone while CRH stimulates the release of adrenocorticotropic hormone. The last two hormones—oxytocin and antidiuretic hormone—are produced by the hypothalamus and transported to the posterior pituitary, where they are stored and later released.
          </p>

        </div>

         <div class="skele">
        <div class="title">Pituitary Gland</div>
        <p>
The pituitary gland, also known as the hypophysis, is a small pea-sized lump of tissue connected to the inferior portion of the hypothalamus of the brain. Many blood vessels surround the pituitary gland to carry the hormones it releases throughout the body. Situated in a small depression in the sphenoid bone called the sella turcica, the pituitary gland is actually made of 2 completely separate structures: the posterior and anterior pituitary glands.

<ol>
<li>Posterior Pituitary: The posterior pituitary gland is actually not glandular tissue at all, but nervous tissue instead. The posterior pituitary is a small extension of the hypothalamus through which the axons of some of the neurosecretory cells of the hypothalamus extend. These neurosecretory cells create 2 hormones in the hypothalamus that are stored and released by the posterior pituitary:  </li>
	<ol>
	<li>Oxytocin triggers uterine contractions during childbirth and the release of milk during breastfeeding.</li>
	<li>
Antidiuretic hormone (ADH) prevents water loss in the body by increasing the re-uptake of water in the kidneys and reducing blood flow to sweat glands.</li>
	</ol>
<li>Anterior Pituitary: The anterior pituitary gland is the true glandular part of the pituitary gland. The function of the anterior pituitary gland is controlled by the releasing and inhibiting hormones of the hypothalamus. The anterior pituitary produces 6 important hormones: </li>
		<ol>
		<li>Thyroid stimulating hormone (TSH), as its name suggests, is a tropic hormone responsible for the stimulation of the thyroid gland.</li>
		<li>Adrenocorticotropic hormone (ACTH) stimulates the adrenal cortex, the outer part of the adrenal gland, to produce its hormones.</li>
		<li>Follicle stimulating hormone (FSH) stimulates the follicle cells of the gonads to produce gametes—ova in females and sperm in males.</li>
		<li>Luteinizing hormone (LH) stimulates the gonads to produce the sex hormones—estrogens in females and testosterone in males.</li>
		<li>Human growth hormone (HGH) affects many target cells throughout the body by stimulating their growth, repair, and reproduction.</li>
		<li>Prolactin (PRL) has many effects on the body, chief of which is that it stimulates the mammary glands of the breast to produce milk.</li>
		</ol>

</ol>
        </p>
        </div>

         <div class="skele">
        <div class="title">Pineal Gland</div>
        <p>
 The pineal gland is a small pinecone-shaped mass of glandular tissue found just posterior to the thalamus of the brain. The pineal gland produces the hormone melatonin that helps to regulate the human sleep-wake cycle known as the circadian rhythm. The activity of the pineal gland is inhibited by stimulation from the photoreceptors of the retina. This light sensitivity causes melatonin to be produced only in low light or darkness. Increased melatonin production causes humans to feel drowsy at nighttime when the pineal gland is active.
        </p>
        </div>

         <div class="skele">
        <div class="title">Thyroid Gland</div>
        <p>
The thyroid gland is a butterfly-shaped gland located at the base of the neck and wrapped around the lateral sides of the trachea. The thyroid gland produces 3 major hormones: 

         <ol>
          <li>Calcitonin</li>
          <li>Triiodothyronine (T3)</li>
          <li>Lumbar (lower back) - 5 vertebrae</li>
          <li>Sacrum - 1 vertebra</li>
          <li>Thyroxine (T4)</li>
          </ol>

Calcitonin is released when calcium ion levels in the blood rise above a certain set point. Calcitonin functions to reduce the concentration of calcium ions in the blood by aiding the absorption of calcium into the matrix of bones. The hormones T3 and T4 work together to regulate the body’s metabolic rate. Increased levels of T3 and T4 lead to increased cellular activity and energy usage in the body.
        </p>
        </div>

 <div class="skele">
        <div class="title">Parathyroid Glands</div>
        <p>
 The parathyroid glands are 4 small masses of glandular tissue found on the posterior side of the thyroid gland. The parathyroid glands produce the hormone parathyroid hormone (PTH), which is involved in calcium ion homeostasis. PTH is released from the parathyroid glands when calcium ion levels in the blood drop below a set point. PTH stimulates the osteoclasts to break down the calcium containing bone matrix to release free calcium ions into the bloodstream. PTH also triggers the kidneys to return calcium ions filtered out of the blood back to the bloodstream so that it is conserved.

        </p>
        </div>


         <div class="skele">
        <div class="title">Adrenal Glands</div>
        <p>
  
The adrenal glands are a pair of roughly triangular glands found immediately superior to the kidneys. The adrenal glands are each made of 2 distinct layers, each with their own unique functions: the outer adrenal cortex and inner adrenal medulla.

<ol>
<li>Adrenal cortex: The adrenal cortex produces many cortical hormones in 3 classes: glucocorticoids, mineralocorticoids, and androgens. </li>
		<ol> 
		<li>Glucocorticoids have many diverse functions, including the breakdown of proteins and lipids to produce glucose. Glucocorticoids also function to reduce inflammation and immune response.</li>
		<li>Mineralocorticoids, as their name suggests, are a group of hormones that help to regulate the concentration of mineral ions in the body.</li>
		<li>Androgens, such as testosterone, are produced at low levels in the adrenal cortex to regulate the growth and activity of cells that are receptive to male hormones. In adult males, the amount of androgens produced by the testes is many times greater than the amount produced by the adrenal cortex, leading to the appearance of male secondary sex characteristics.</li>
		</ol>
<li>Adrenal medulla: The adrenal medulla produces the hormones epinephrine and norepinephrine under stimulation by the sympathetic division of the autonomic nervous system. Both of these hormones help to increase the flow of blood to the brain and muscles to improve the “fight-or-flight” response to stress. These hormones also work to increase heart rate, breathing rate, and blood pressure while decreasing the flow of blood to and function of organs that are not involved in responding to emergencies.</li>
</ol>

        </p>
        </div>


         <div class="skele">
        <div class="title">Pancreas</div>
        <p>
The pancreas is a large gland located in the abdominal cavity just inferior and posterior to the stomach. The pancreas is considered to be a heterocrine gland as it contains both endocrine and exocrine tissue. The endocrine cells of the pancreas make up just about 1% of the total mass of the pancreas and are found in small groups throughout the pancreas called islets of Langerhans. Within these islets are 2 types of cells—alpha and beta cells. The alpha cells produce the hormone glucagon, which is responsible for raising blood glucose levels. Glucagon triggers muscle and liver cells to break down the polysaccharide glycogen to release glucose into the bloodstream. The beta cells produce the hormone insulin, which is responsible for lowering blood glucose levels after a meal. Insulin triggers the absorption of glucose from the blood into cells, where it is added to glycogen molecules for storage.

        </p>
        </div>


         <div class="skele">
        <div class="title">Gonads</div>
        <p>
The gonads—ovaries in females and testes in males—are responsible for producing the sex hormones of the body. These sex hormones determine the secondary sex characteristics of adult females and adult males.

<ol>
  <li>Testes: The testes are a pair of ellipsoid organs found in the scrotum of males that produce the androgen testosterone in males after the start of puberty. Testosterone has effects on many parts of the body, including the muscles, bones, sex organs, and hair follicles. This hormone causes growth and increases in strength of the bones and muscles, including the accelerated growth of long bones during adolescence. During puberty, testosterone controls the growth and development of the sex organs and body hair of males, including pubic, chest, and facial hair. In men who have inherited genes for baldness testosterone triggers the onset of androgenic alopecia, commonly known as male pattern baldness.</li>
  <li>Ovaries: The ovaries are a pair of almond-shaped glands located in the pelvic body cavity lateral and superior to the uterus in females. The ovaries produce the female sex hormones progesterone and estrogens. Progesterone is most active in females during ovulation and pregnancy where it maintains appropriate conditions in the human body to support a developing fetus. Estrogens are a group of related hormones that function as the primary female sex hormones. The release of estrogen during puberty triggers the development of female secondary sex characteristics such as uterine development, breast development, and the growth of pubic hair. Estrogen also triggers the increased growth of bones during adolescence that lead to adult height and proportions.</li>
</ol>

        </p>
        </div>

               <div class="skele">
        <div class="title">Thymus</div>
        <p>The thymus is a soft, triangular-shaped organ found in the chest posterior to the sternum. The thymus produces hormones called thymosins that help to train and develop T-lymphocytes during fetal development and childhood. The T-lymphocytes produced in the thymus go on to protect the body from pathogens throughout a person’s entire life. The thymus becomes inactive during puberty and is slowly replaced by adipose tissue throughout a person’s life.</p>
        <p>
  Other Hormone Producing Organs
In addition to the glands of the endocrine system, many other non-glandular organs and tissues in the body produce hormones as well. 
  <ol>
    <li>Heart: The cardiac muscle tissue of the heart is capable of producing the hormone atrial natriuretic peptide (ANP) in response to high blood pressure levels. ANP works to reduce blood pressure by triggering vasodilation to provide more space for the blood to travel through. ANP also reduces blood volume and pressure by causing water and salt to be excreted out of the blood by the kidneys.</li>

    <li>Kidneys: The kidneys produce the hormone erythropoietin (EPO) in response to low levels of oxygen in the blood. EPO released by the kidneys travels to the red bone marrow where it stimulates an increased production of red blood cells. The number of red blood cells increases the oxygen carrying capacity of the blood, eventually ending the production of EPO.</li>

    <li>
Adipose: Adipose tissue produces the hormone leptin that is involved in the management of appetite and energy usage by the body. Leptin is produced at levels relative to the amount of adipose tissue in the body, allowing the brain to monitor the body’s energy storage condition. When the body contains a sufficient level of adipose for energy storage, the level of leptin in the blood tells the brain that the body is not starving and may work normally. If the level of adipose or leptin decreases below a certain threshold, the body enters starvation mode and attempts to conserve energy through increased hunger and food intake and decreased energy usage. Adipose tissue also produces very low levels of estrogens in both men and women. In obese people the large volume of adipose tissue may lead to abnormal estrogen levels.</li>

    <li>Placenta: In pregnant women, the placenta produces several hormones that help to maintain pregnancy. Progesterone is produced to relax the uterus, protect the fetus from the mother’s immune system, and prevent premature delivery of the fetus. Human chorionic gonadotropin (HCG) assists progesterone by signaling the ovaries to maintain the production of estrogen and progesterone throughout pregnancy.</li>

    <li>Local Hormones: Prostaglandins and leukotrienes are produced by every tissue in the body (except for blood tissue) in response to damaging stimuli. These two hormones mainly affect the cells that are local to the source of damage, leaving the rest of the body free to function normally. </li>
    <ol>
    <li>Prostaglandins cause swelling, inflammation, increased pain sensitivity, and increased local body temperature to help block damaged regions of the body from infection or further damage. They act as the body’s natural bandages to keep pathogens out and swell around damaged joints like a natural cast to limit movement. </li>
    <li>Leukotrienes help the body heal after prostaglandins have taken effect by reducing inflammation while helping white blood cells to move into the region to clean up pathogens and damaged tissues.</li>
    </ol>

  </ol>

        </p>
