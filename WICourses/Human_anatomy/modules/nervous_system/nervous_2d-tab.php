        <div class="title">Nervous System</div>
        <p>
The nervous system consists of the brain, spinal cord, sensory organs, and all of the nerves that connect these organs with the rest of the body. Together, these organs are responsible for the control of the body and communication among its parts. The brain and spinal cord form the control center known as the central nervous system (CNS), where information is evaluated and decisions made. The sensory nerves and sense organs of the peripheral nervous system (PNS) monitor conditions inside and outside of the body and send this information to the CNS. Efferent nerves in the PNS carry signals from the control center to the muscles, glands, and organs to regulate their functions.
        </p>
        </div>

        <div class="skele">
        <div class="title">Nervous Tissue</div>
        <p>
The majority of the nervous system is tissue made up of two classes of cells: neurons and neuroglia.


          <ol>
          <li>Neurons. Neurons, also known as nerve cells, communicate within the body by transmitting electrochemical signals. Neurons look quite different from other cells in the body due to the many long cellular processes that extend from their central cell body. The cell body is the roughly round part of a neuron that contains the nucleus, mitochondria, and most of the cellular organelles. Small tree-like structures called dendrites extend from the cell body to pick up stimuli from the environment, other neurons, or sensory receptor cells. Long transmitting processes called axons extend from the cell body to send signals onward to other neurons or effector cells in the body. </li>
          <p>There are 3 basic classes of neurons: afferent neurons, efferent neurons, and interneurons.</p>
          <ol> 
          <li>Afferent neurons. Also known as sensory neurons, afferent neurons transmit sensory signals to the central nervous system from receptors in the body.</li>
           <li>Efferent neurons. Also known as motor neurons, efferent neurons transmit signals from the central nervous system to effectors in the body such as muscles and glands.</li>
            <li>Interneurons. Interneurons form complex networks within the central nervous system to integrate the information received from afferent neurons and to direct the function of the body through efferent neurons.</li>
          </ol>
          <li>Neuroglia. Neuroglia, also known as glial cells, act as the “helper” cells of the nervous system. Each neuron in the body is surrounded by anywhere from 6 to 60 neuroglia that protect, feed, and insulate the neuron. Because neurons are extremely specialized cells that are essential to body function and almost never reproduce, neuroglia are vital to maintaining a functional nervous system.</li>

          </ol>

        </p>

        </div>

         <div class="skele">
        <div class="title">Brain</div>
        <p>
The brain, a soft, wrinkled organ that weighs about 3 pounds, is located inside the cranial cavity, where the bones of the skull surround and protect it. The approximately 100 billion neurons of the brain form the main control center of the body. The brain and spinal cord together form the central nervous system (CNS), where information is processed and responses originate. The brain, the seat of higher mental functions such as consciousness, memory, planning, and voluntary actions, also controls lower body functions such as the maintenance of respiration, heart rate, blood pressure, and digestion.
        </p>
        </div>

         <div class="skele">
        <div class="title">Spinal Cord</div>
        <p>
The spinal cord is a long, thin mass of bundled neurons that carries information through the vertebral cavity of the spine beginning at the medulla oblongata of the brain on its superior end and continuing inferiorly to the lumbar region of the spine. In the lumbar region, the spinal cord separates into a bundle of individual nerves called the cauda equina (due to its resemblance to a horse’s tail) that continues inferiorly to the sacrum and coccyx. The white matter of the spinal cord functions as the main conduit of nerve signals to the body from the brain. The grey matter of the spinal cord integrates reflexes to stimuli.
        </p>
        </div>

         <div class="skele">
        <div class="title">Nerves</div>
        <p>
       Nerves are bundles of axons in the peripheral nervous system (PNS) that act as information highways to carry signals between the brain and spinal cord and the rest of the body. Each axon is wrapped in a connective tissue sheath called the endoneurium. Individual axons of the nerve are bundled into groups of axons called fascicles, wrapped in a sheath of connective tissue called the perineurium. Finally, many fascicles are wrapped together in another layer of connective tissue called the epineurium to form a whole nerve. The wrapping of nerves with connective tissue helps to protect the axons and to increase the speed of their communication within the body.

         <ol>
          <li>Afferent, Efferent, and Mixed Nerves. Some of the nerves in the body are specialized for carrying information in only one direction, similar to a one-way street. Nerves that carry information from sensory receptors to the central nervous system only are called afferent nerves. Other neurons, known as efferent nerves, carry signals only from the central nervous system to effectors such as muscles and glands. Finally, some nerves are mixed nerves that contain both afferent and efferent axons. Mixed nerves function like 2-way streets where afferent axons act as lanes heading toward the central nervous system and efferent axons act as lanes heading away from the central nervous system.</li>
          <li>Cranial Nerves. Extending from the inferior side of the brain are 12 pairs of cranial nerves. Each cranial nerve pair is identified by a Roman numeral 1 to 12 based upon its location along the anterior-posterior axis of the brain. Each nerve also has a descriptive name (e.g. olfactory, optic, etc.) that identifies its function or location. The cranial nerves provide a direct connection to the brain for the special sense organs, muscles of the head, neck, and shoulders, the heart, and the GI tract.</li>
          <li>Spinal Nerves. Extending from the left and right sides of the spinal cord are 31 pairs of spinal nerves. The spinal nerves are mixed nerves that carry both sensory and motor signals between the spinal cord and specific regions of the body. The 31 spinal nerves are split into 5 groups named for the 5 regions of the vertebral column. Thus, there are 8 pairs of cervical nerves, 12 pairs of thoracic nerves, 5 pairs of lumbar nerves, 5 pairs of sacral nerves, and 1 pair of coccygeal nerves. Each spinal nerve exits from the spinal cord through the intervertebral foramen between a pair of vertebrae or between the C1 vertebra and the occipital bone of the skull.</li>

          </ol>

        </p>
        </div>

 <div class="skele">
        <div class="title">Meninges</div>
        <p>
The meninges are the protective coverings of the central nervous system (CNS). They consist of three layers: the dura mater, arachnoid mater, and pia mater.
<ol>
<li>Dura mater. The dura mater, which means “tough mother,” is the thickest, toughest, and most superficial layer of meninges. Made of dense irregular connective tissue, it contains many tough collagen fibers and blood vessels. Dura mater protects the CNS from external damage, contains the cerebrospinal fluid that surrounds the CNS, and provides blood to the nervous tissue of the CNS.</li>

<li>Arachnoid mater. The arachnoid mater, which means “spider-like mother,” is much thinner and more delicate than the dura mater. It lines the inside of the dura mater and contains many thin fibers that connect it to the underlying pia mater. These fibers cross a fluid-filled space called the subarachnoid space between the arachnoid mater and the pia mater.</li>
<li>Pia mater. The pia mater, which means “tender mother,” is a thin and delicate layer of tissue that rests on the outside of the brain and spinal cord. Containing many blood vessels that feed the nervous tissue of the CNS, the pia mater penetrates into the valleys of the sulci and fissures of the brain as it covers the entire surface of the CNS.</li>
</ol>

        </p>
        </div>


         <div class="skele">
        <div class="title">Cerebrospinal Fluid</div>
        <p>
The space surrounding the organs of the CNS is filled with a clear fluid known as cerebrospinal fluid (CSF). CSF is formed from blood plasma by special structures called choroid plexuses. The choroid plexuses contain many capillaries lined with epithelial tissue that filters blood plasma and allows the filtered fluid to enter the space around the brain. 
Newly created CSF flows through the inside of the brain in hollow spaces called ventricles and through a small cavity in the middle of the spinal cord called the central canal. CSF also flows through the subarachnoid space around the outside of the brain and spinal cord. CSF is constantly produced at the choroid plexuses and is reabsorbed into the bloodstream at structures called arachnoid villi.

Cerebrospinal fluid provides several vital functions to the central nervous system:

<ol> 
<li>CSF absorbs shocks between the brain and skull and between the spinal cord and vertebrae. This shock absorption protects the CNS from blows or sudden changes in velocity, such as during a car accident.</li>
<li>The brain and spinal cord float within the CSF, reducing their apparent weight through buoyancy. The brain is a very large but soft organ that requires a high volume of blood to function effectively. The reduced weight in cerebrospinal fluid allows the blood vessels of the brain to remain open and helps protect the nervous tissue from becoming crushed under its own weight.</li>
<li>CSF helps to maintain chemical homeostasis within the central nervous system. It contains ions, nutrients, oxygen, and albumins that support the chemical and osmotic balance of nervous tissue. CSF also removes waste products that form as byproducts of cellular metabolism within nervous tissue.</li>
</ol>
        </p>
        </div>


         <div class="skele">
        <div class="title">Sense Organs</div>
        <p>
All of the bodies’ many sense organs are components of the nervous system. What are known as the special senses—vision, taste, smell, hearing, and balance—are all detected by specialized organs such as the eyes, taste buds, and olfactory epithelium. Sensory receptors for the general senses like touch, temperature, and pain are found throughout most of the body. All of the sensory receptors of the body are connected to afferent neurons that carry their sensory information to the CNS to be processed and integrated.

        </p>
        </div>


         <div class="skele">
        <div class="title">Physiology</div>
        <p>
The nervous system has 3 main functions: sensory, integration, and motor. 

<ol>
  <li>Sensory. The sensory function of the nervous system involves collecting information from sensory receptors that monitor the body’s internal and external conditions. These signals are then passed on to the central nervous system (CNS) for further processing by afferent neurons (and nerves).</li>
  <li>Integration. The process of integration is the processing of the many sensory signals that are passed into the CNS at any given time. These signals are evaluated, compared, used for decision making, discarded or committed to memory as deemed appropriate. Integration takes place in the gray matter of the brain and spinal cord and is performed by interneurons. Many interneurons work together to form complex networks that provide this processing power.</li>
  <li>Motor. Once the networks of interneurons in the CNS evaluate sensory information and decide on an action, they stimulate efferent neurons. Efferent neurons (also called motor neurons) carry signals from the gray matter of the CNS through the nerves of the peripheral nervous system to effector cells. The effector may be smooth, cardiac, or skeletal muscle tissue or glandular tissue. The effector then releases a hormone or moves a part of the body to respond to the stimulus.</li>
</ol>

        </p>
        </div>

               <div class="skele">
        <div class="title">Divisions of the Nervous System</div>
        <div class="title">Central Nervous System</div>

        <p>
The brain and spinal cord together form the central nervous system, or CNS. The CNS acts as the control center of the body by providing its processing, memory, and regulation systems. The CNS takes in all of the conscious and subconscious sensory information from the body’s sensory receptors to stay aware of the body’s internal and external conditions. Using this sensory information, it makes decisions about both conscious and subconscious actions to take to maintain the body’s homeostasis and ensure its survival. The CNS is also responsible for the higher functions of the nervous system such as language, creativity, expression, emotions, and personality. The brain is the seat of consciousness and determines who we are as individuals.

        </p>
        </div>


               <div class="skele">
        <div class="title">Peripheral Nervous System</div>
        <p>
The peripheral nervous system (PNS) includes all of the parts of the nervous system outside of the brain and spinal cord. These parts include all of the cranial and spinal nerves, ganglia, and sensory receptors.

        </p>
        </div>

               <div class="skele">
        <div class="title">Somatic Nervous System</div>
        <p>
The somatic nervous system (SNS) is a division of the PNS that includes all of the voluntary efferent neurons. The SNS is the only consciously controlled part of the PNS and is responsible for stimulating skeletal muscles in the body.

        </p>

        </div>

                       <div class="skele">
        <div class="title">Autonomic Nervous System</div>
        <p>
The autonomic nervous system (ANS) is a division of the PNS that includes all of the involuntary efferent neurons. The ANS controls subconscious effectors such as visceral muscle tissue, cardiac muscle tissue, and glandular tissue.

There are 2 divisions of the autonomic nervous system in the body: the sympathetic and parasympathetic divisions.

<ol>
<li>Sympathetic. The sympathetic division forms the body’s “fight or flight” response to stress, danger, excitement, exercise, emotions, and embarrassment. The sympathetic division increases respiration and heart rate, releases adrenaline and other stress hormones, and decreases digestion to cope with these situations.</li>
<li>Parasympathetic. The parasympathetic division forms the body’s “rest and digest” response when the body is relaxed, resting, or feeding. The parasympathetic works to undo the work of the sympathetic division after a stressful situation. Among other functions, the parasympathetic division works to decrease respiration and heart rate, increase digestion, and permit the elimination of wastes.</li>
</ol>

        </p>

        </div>


                       <div class="skele">
        <div class="title">Enteric Nervous System</div>
        <p>
The enteric nervous system (ENS) is the division of the ANS that is responsible for regulating digestion and the function of the digestive organs. The ENS receives signals from the central nervous system through both the sympathetic and parasympathetic divisions of the autonomic nervous system to help regulate its functions. However, the ENS mostly works independently of the CNS and continues to function without any outside input. For this reason, the ENS is often called the “brain of the gut” or the body’s “second brain.” The ENS is an immense system—almost as many neurons exist in the ENS as in the spinal cord.

        </p>
        </div>


         <div class="skele">
        <div class="title">Action Potentials</div>
        <p>
Neurons function through the generation and propagation of electrochemical signals known as action potentials (APs). An AP is created by the movement of sodium and potassium ions through the membrane of neurons. (See Water and Electrolytes.)

<ol>
<li>Resting Potential. At rest, neurons maintain a concentration of sodium ions outside of the cell and potassium ions inside of the cell. This concentration is maintained by the sodium-potassium pump of the cell membrane which pumps 3 sodium ions out of the cell for every 2 potassium ions that are pumped into the cell. The ion concentration results in a resting electrical potential of -70 millivolts (mV), which means that the inside of the cell has a negative charge compared to its surroundings.</li>

<li>Threshold Potential. If a stimulus permits enough positive ions to enter a region of the cell to cause it to reach -55 mV, that region of the cell will open its voltage-gated sodium channels and allow sodium ions to diffuse into the cell. -55 mV is the threshold potential for neurons as this is the “trigger” voltage that they must reach to cross the threshold into forming an action potential.</li>

<li>Depolarization. Sodium carries a positive charge that causes the cell to become depolarized (positively charged) compared to its normal negative charge. The voltage for depolarization of all neurons is +30 mV. The depolarization of the cell is the AP that is transmitted by the neuron as a nerve signal. The positive ions spread into neighboring regions of the cell, initiating a new AP in those regions as they reach -55 mV. The AP continues to spread down the cell membrane of the neuron until it reaches the end of an axon.</li>

<li>Repolarization. After the depolarization voltage of +30 mV is reached, voltage-gated potassium ion channels open, allowing positive potassium ions to diffuse out of the cell. The loss of potassium along with the pumping of sodium ions back out of the cell through the sodium-potassium pump restores the cell to the -55 mV resting potential. At this point the neuron is ready to start a new action potential.</li>
</ol>

        </p>

        </div>


           <div class="skele">
        <div class="title">Synapses</div>
        <p>
A synapse is the junction between a neuron and another cell. Synapses may form between 2 neurons or between a neuron and an effector cell. There are two types of synapses found in the body: chemical synapses and electrical synapses.

<ol>  
<li>Chemical synapses. At the end of a neuron’s axon is an enlarged region of the axon known as the axon terminal. The axon terminal is separated from the next cell by a small gap known as the synaptic cleft. When an AP reaches the axon terminal, it opens voltage-gated calcium ion channels. Calcium ions cause vesicles containing chemicals known as neurotransmitters (NT) to release their contents by exocytosis into the synaptic cleft. The NT molecules cross the synaptic cleft and bind to receptor molecules on the cell, forming a synapse with the neuron. These receptor molecules open ion channels that may either stimulate the receptor cell to form a new action potential or may inhibit the cell from forming an action potential when stimulated by another neuron.</li>
<li>Electrical synapses. Electrical synapses are formed when 2 neurons are connected by small holes called gap junctions. The gap junctions allow electric current to pass from one neuron to the other, so that an AP in one cell is passed directly on to the other cell through the synapse.</li>
</ol>
        </p>
        </div>


          <div class="skele">
        <div class="title">Myelination</div>
        <p>
The axons of many neurons are covered by a coating of insulation known as myelin to increase the speed of nerve conduction throughout the body. Myelin is formed by 2 types of glial cells: Schwann cells in the PNS and oligodendrocytes in the CNS. In both cases, the glial cells wrap their plasma membrane around the axon many times to form a thick covering of lipids. The development of these myelin sheaths is known as myelination.

Myelination speeds up the movement of APs in the axon by reducing the number of APs that must form for a signal to reach the end of an axon. The myelination process begins speeding up nerve conduction in fetal development and continues into early adulthood. Myelinated axons appear white due to the presence of lipids and form the white matter of the inner brain and outer spinal cord. White matter is specialized for carrying information quickly through the brain and spinal cord. The gray matter of the brain and spinal cord are the unmyelinated integration centers where information is processed.

        </p>
        </div>


          <div class="skele">
        <div class="title">Reflexes</div>
        <p>
Reflexes are fast, involuntary responses to stimuli. The most well known reflex is the patellar reflex, which is checked when a physicians taps on a patient’s knee during a physical examination. Reflexes are integrated in the gray matter of the spinal cord or in the brain stem. Reflexes allow the body to respond to stimuli very quickly by sending responses to effectors before the nerve signals reach the conscious parts of the brain. This explains why people will often pull their hands away from a hot object before they realize they are in pain.

        </p>
        </div>


               <div class="skele">
        <div class="title">Functions of the Cranial Nerves</div>
        <p>
Each of the 12 cranial nerves has a specific function within the nervous system.

<ol> 

<li>The olfactory nerve (I) carries scent information to the brain from the olfactory epithelium in the roof of the nasal cavity. </li>
<li>The optic nerve (II) carries visual information from the eyes to the brain. </li>
<li>Oculomotor, trochlear, and abducens nerves (III, IV, and VI) all work together to allow the brain to control the movement and focus of the eyes. The trigeminal nerve (V) carries sensations from the face and innervates the muscles of mastication. </li>
<li>The facial nerve (VII) innervates the muscles of the face to make facial expressions and carries taste information from the anterior 2/3 of the tongue. </li>
<li>The vestibulocochlear nerve (VIII) conducts auditory and balance information from the ears to the brain. </li>
<li>
The glossopharyngeal nerve (IX) carries taste information from the posterior 1/3 of the tongue and assists in swallowing. </li>
<li>The vagus nerve (X), sometimes called the wandering nerve due to the fact that it innervates many different areas, “wanders” through the head, neck, and torso. It carries information about the condition of the vital organs to the brain, delivers motor signals to control speech and delivers parasympathetic signals to many organs.</li>
<li>The accessory nerve (XI) controls the movements of the shoulders and neck. 
</li>
<li>The hypoglossal nerve (XII) moves the tongue for speech and swallowing.</li>
</ol>
        </p>
        </div>


                       <div class="skele">
        <div class="title">Sensory Physiology</div>
        <p>
All sensory receptors can be classified by their structure and by the type of stimulus that they detect. Structurally, there are 3 classes of sensory receptors: free nerve endings, encapsulated nerve endings, and specialized cells. Free nerve endings are simply free dendrites at the end of a neuron that extend into a tissue. Pain, heat, and cold are all sensed through free nerve endings. An encapsulated nerve ending is a free nerve ending wrapped in a round capsule of connective tissue. When the capsule is deformed by touch or pressure, the neuron is stimulated to send signals to the CNS. Specialized cells detect stimuli from the 5 special senses: vision, hearing, balance, smell, and taste. Each of the special senses has its own unique sensory cells—such as rods and cones in the retina to detect light for the sense of vision.

Functionally, there are 6 major classes of receptors: mechanoreceptors, nociceptors, photoreceptors, chemoreceptors, osmoreceptors, and thermoreceptors.  

<ol> 

<li>Mechanoreceptors. Mechanoreceptors are sensitive to mechanical stimuli like touch, pressure, vibration, and blood pressure. </li>
<li>Nociceptors. Nociceptors respond to stimuli such as extreme heat, cold, or tissue damage by sending pain signals to the CNS.  </li>
<li>Photoreceptors. Photoreceptors in the retina detect light to provide the sense of vision. </li>

<li>Chemoreceptors. Chemoreceptors detect chemicals in the bloodstream and provide the senses of taste and smell.</li>

<li>Osmoreceptors. Osmoreceptors monitor the osmolarity of the blood to determine the body's hydration levels. </li>

<li>
Thermoreceptors. Thermoreceptors detect temperatures inside the body and in its surroundings. </li>

</ol>
        </p>
