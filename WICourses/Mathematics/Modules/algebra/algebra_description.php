<div class="title">Algebra</div>
<div class="col-md-12">
<p>
In its most general form, algebra is the study of mathematical symbols and the rules for manipulating these symbols; it is a unifying thread of almost all of mathematics. As such, it includes everything from elementary equation solving to the study of abstractions such as groups, rings, and fields. </p>
</div>

<div class="title">BODMAS</div>
<div class="col-md-12">
<p>
The original order of operations in some countries was BODMAS, which stands for:
<ol>
<li>Brackets</li>
<li>Orders or pOwers</li>
<li>Division</li>
<li>Multiplication</li>
<li>Addition</li>
<li>Subtraction</li>
</ol>
 The O is sometimes associated with Of. This mnemonic was common until exponentials were added into the mnemonic. </p>
</div>