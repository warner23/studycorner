  <h4>Our Mission</h4>									
  <p>We are a charity providing relief and support for persons, who have suffered or are in danger of suffering from social and emotional difficulties including sex related violence or abuse, their children and dependants by the provision of food, clothing, furniture, friendship, training and other assistance.
 
Our primary aim is to bring hope and support to those facing or fleeing Domestic Violence and to empower them to create a better life for themselves. We believe in a safe place with structure group work, it supports in rebuilding our clients lives through our flagship TEN(Training, Enterprise, Nurture) scheme. 63 people benefited from this scheme in 2014-15. Some of our clients self-refer, some are referred by other agencies familiar with the range of services we offer.
 
The organisation is led by a board of trustees and volunteers that are passionate about restoring hope, empowering people, and helping women realise their true potential and self-worth. Our approach is person centred and holistic, working with individuals.
 
Our values: We are motivated by our Christian faith and beliefs, believing that people are entitled to a wonderful and abundant life. Respect, dignity, integrity and freedom are our core values that we hold and releasing people to realise their potential.
 
We work with people with multiple complex needs, from being vulnerable and homeless as a result of fleeing domestic violence, people undergoing trauma and abuse, involved in prostitution, alcohol and drug abuse, being in debt, self harm, and in fear of their lives.</p>								
